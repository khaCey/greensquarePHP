                            こちらはシャムトーク・Sham Talk 英会話ポッドキャストのページです。<br><br>

                            ん？何で英会話ポッドキャストを作ったかって？いきなりいい質問ですね！<br><br>

                            英語を学びたい人は日本でも年々増えていますが、リスニングの機会を増やすのはなかなか難しいですよね。ラジオ英会話やYouTubeでも英会話チャンネルは多々ありますが、日本語を多く使っていたり、英語がとても早口だったり、なかなか自分に合ったものを探せずにいる方も多いのではないでしょうか。勉強を続けるモチベーションを維持するためには、あなたにぴったりの勉強方法や教材を見つけるのが一番の近道です。<br><br>

                            Sham Talk(シャムトーク)はトークショースタイルの英会話ポッドキャスト。いろいろなレベルの方に理解してもらえるよう、早すぎず、遅すぎずのペースで話すことを心がけています。ホストは私、シャム（シャム猫のシャム。前世は猫かも）。イギリスはロンドン出身で、日本にきて８年目になります。様々なテーマについて話して行くので、お好きなトピックから聞いてみてください。ダイエット、アニメ、映画、ビジネス、政治、テクノロジー、スポーツ、ファションなどのお題を予定していますが、リクエストも受け付けています！<br><br>

