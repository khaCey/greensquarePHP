        <h3 class="lessonLabel">ーレッスン内容についてー</h3>
        <br>
        <h4 class="question">Q．まったくの初心者なので、レッスンについていけるか不安なのですが…</h4><br>
        <p class="answer">A．無料体験レッスンにてレベルチェックをさせていただき、おひとりおひとりに合った難易度でレッスンを進めさせていただきますのでご安心ください。また、日本人スタッフもおりますので何か不安なことや心配なことがございましたら、いつでもなんでもご相談ください。</p><br><br>
        <h4 class="question">Q．急いで英会話を身につけたいけれど、短期集中コースはありますか？</h4><br>
        <p class="answer">A．当校には月2回、4回、8回コースの3コースがございます。ご希望の方は月8回コースの料金で8回以上のご受講が可能です。より効果的な英語学習のために1日に2レッスンずつご受講されることをおすすめします。</p><br><br>

        <h4 class="question">Q．どのような教材を使うのですか？</h4><br>
        <p class="answer">A．当校では、デジタル教科書を無料でご提供しております。どこでも、どのデバイスからでもアクセス可能なので、お手軽に教科書を持ち運ぶことができます。紙の教科書のご使用を希望される場合にはプリントアウトすることも可能です。</p><br><br>
        <h4 class="question">Q．入会費や教材費はかかりますか？</h4><br>
        <p>A．入会費や教材費は一切いただいておりません。当校でかかる費用はレッスン代とドリンクおよびお食事代のみです。</p><br><br>

        <h4 class="question">Q．英会話レッスン以外に予習や復習は必要ですか？</h4><br>
        <p class="answer">A．当校では生徒さんに合わせてホームワークを用意しております。また、より有効な英語学習のために、予習復習をおすすめしております。</p><br><br>


