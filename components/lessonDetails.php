        <h3>ー無料体験レッスンについてー</h3><br>

        <h4 class="question">Q．無料体験を受けずに有料会員登録できますか？</h4><br>
        <p class="answer">A．有料会員登録はいつでも可能ですが、無料体験レッスンにて当校のメソッド、指導方法、使用教材がご受講者様に合っているかどうかを確認していただき、また本レッスンへのスムーズなご案内のために　ご受講者様のレベルチェックもさせていただいております。ぜひ、無料体験レッスンのご受講をご検討ください。</p><br><br>

        <h4 class="question">Q．無料体験レッスンを受けずに有料会員になると無料体験レッスンはできませんか？</h4><br>
        <p class="answer">A．無料体験レッスンを受ける前にご入会いただいても、本レッスン開始前でしたら無料体験レッスンを受けていただくことは可能です。しかし、無料体験レッスンでは当校のメソッドや指導方法、使用教材を知っていただく事や、ご受講者様のレベルチェックを兼ねております。そのため、ご入会前の無料体験レッスンのご受講をおすすめしております。</p><br><br>


